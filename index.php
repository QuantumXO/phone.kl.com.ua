$('.slider').mobilyslider({
	transition: 'fade',
	animationSpeed: 800,
	autoplay: true,
	bullets: false,
	pauseOnHover: true,
	arrowsHide: false
});

$(document).ready(function(){
	$("#navigation").on("click","a", function (event) {
		//отменяем стандартную обработку нажатия по ссылке
		event.preventDefault();
		//забираем идентификатор бока с атрибута href
		var id  = $(this).attr('href'),
		//узнаем высоту от начала страницы до блока на который ссылается якорь
			top = $(id).offset().top,
			top = (top - 80);
		//анимируем переход на расстояние - top за 1500 мс
		$('body,html').animate({scrollTop: top}, 1500);
	});
	
	$('.js-btn').hover(
		function(){
			$(this).removeClass('animated');
		},
		function(){
			$(this).addClass('animated');
	});

	$('.cbalink').css({'display' : 'none'});
	
	var $menu = $("#header_top");
	$(window).scroll(function(){
		if ( $(this).scrollTop() > 50 ){
			$menu.addClass("topWindow");
		} else if($(this).scrollTop() <= 50 && $menu.hasClass("topWindow")) {
			$menu.removeClass("topWindow");
		}
	});

	$("#navbar-mobile").click(function() {
	  $("#navigation").toggleClass("visible");
	});
});
	
$(document).mouseup(function(e) {
  var $target = $(e.target);
  if ($target.closest("#navigation").length == 0 && $target.closest("#navbar-mobile").length == 0) {
    $("#navigation").removeClass("visible");
  }
});

</script>
